package com.vsc.branch;

public class Branch {
	
	int branch_id;
	String branch_name;
	String branch_postal_code;
	String branch_code;
	String branch_location;
	
	public int getBranch_id() {
		return branch_id;
	}
	public void setBranch_id(int branch_id) {
		this.branch_id = branch_id;
	}
	public String getBranch_name() {
		return branch_name;
	}
	public void setBranch_name(String branch_name) {
		this.branch_name = branch_name;
	}
	public String getBranch_postal_code() {
		return branch_postal_code;
	}
	public void setBranch_postal_code(String branch_postal_code) {
		this.branch_postal_code = branch_postal_code;
	}
	public String getBranch_code() {
		return branch_code;
	}
	public void setBranch_code(String branch_code) {
		this.branch_code = branch_code;
	}
	public String getBranch_location() {
		return branch_location;
	}
	public void setBranch_location(String branch_location) {
		this.branch_location = branch_location;
	}
	
	
	 

}
